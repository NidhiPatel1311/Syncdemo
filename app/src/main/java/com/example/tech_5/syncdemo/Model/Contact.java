package com.example.tech_5.syncdemo.Model;

public class Contact {

    String name;
    String mname;
    String lname;
    String mobileno;

    public Contact() {
    }

    public String getName() {
        return name;
    }

    public String getMname() {
        return mname;
    }

    public String getLname() {
        return lname;
    }

    public String getMobileno() {
        return mobileno;
    }
}
